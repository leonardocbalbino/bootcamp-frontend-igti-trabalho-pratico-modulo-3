export default (valor: number) => {
  if (valor <= 1045) return (valor * 7.5) / 100;
  if (valor <= 2089.6) return (1045 * 7.5) / 100 + ((valor - 1045) * 9) / 100;
  if (valor <= 3134.4)
    return (
      (1045 * 7.5) / 100 +
      ((2089.6 - 1045) * 9) / 100 +
      ((valor - 2089.6) * 12) / 100
    );
  if (valor <= 6101.06) {
    return (
      (1045 * 7.5) / 100 +
      ((2089.6 - 1045) * 9) / 100 +
      ((3134.4 - 2089.6) * 12) / 100 +
      ((valor - 3134.4) * 14) / 100
    );
  }
  return 713.1;
};
